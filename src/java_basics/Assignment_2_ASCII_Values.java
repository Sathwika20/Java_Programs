package java_basics;

public class Assignment_2_ASCII_Values {
    public static void main(String[] args){
        char value1 = 'A';
        char value2 = 'a';
        char value3 = '0';
        char value4 = '@';
        System.out.println("ASCII Value of A is:"+(int) value1);
        System.out.println("ASCII Value of a is:"+(int)value2);
        System.out.println("ASCII Value of 0 is:"+(int) value3);
        System.out.println("ASCII Value of @ is:"+(int)value4);
    }
}
